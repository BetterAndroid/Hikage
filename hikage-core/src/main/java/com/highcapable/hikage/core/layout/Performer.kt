/**
 * Provide a new [View] instance [V].
 * @param viewClass the view class.
 * @param factory the view factory, used to create the view instance.
 * @param lparams the view layout params.
 * @param id the view id, generated by default.
 * @param attrs the attributes body.
 * @param init the view initialization body.
 * @return [V]
 */
@Hikageable
fun <V : View, LP : ViewGroup.LayoutParams> Hikage.Performer<LP>.View(
    viewClass: KClass<V>,
    factory: ViewConstructor<V>? = null,
    lparams: LayoutParams? = null,
    id: String? = null,
    attrs: HikageAttribute = {},
    init: HikageView<V> = {}
) = resolveContext().View(viewClass, factory, lparams, id, attrs, init)

