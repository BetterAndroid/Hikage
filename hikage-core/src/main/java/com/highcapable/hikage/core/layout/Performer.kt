/**
 * Provide layout from another [Hikage].
 *
 * - Note: [Hikage] view IDs will not copy to this layout
 *   when provides a complete [Hikage] instead of [Hikage.Delegate].
 *
 * ```kotlin
 * val first: Hikage = Hikageable(context) {
 *     TextView(id = "textView")
 * }
 * val second: Hikage = Hikageable(context) {
 *     TextView(id = "textView")
 *     // The view ID "textView" will not copied to this layout,
 *     // so there will be no problem of ID overlap.
 *     Layout(first)
 * }
 * ```
 * @param hikage the hikage instance.
 * @param lparams the view layout params.
 * @param id the view id, generated by default.
 * @return [Hikage]
 */
@Hikageable
fun <LP : ViewGroup.LayoutParams> Hikage.Performer<LP>.Layout(
    hikage: Hikage,
    lparams: LayoutParams? = null,
    id: String? = null
) = resolveContext().Layout(hikage, lparams, id)

/**
 * Provide layout from another [Hikage.Delegate].
 *
 * - Note: If you use [embedded], you must make sure that the introduced view IDs
 *   do not overlap with the view IDs in the current layout.
 * @param delegate the delegate instance.
 * @param lparams the view layout params.
 * @param id the view id, generated by default.
 * @param embedded whether to embed this layout (All view IDs will be copied),
 * default is true.
 * @return [Hikage]
 */
@Hikageable
fun <LP : ViewGroup.LayoutParams> Hikage.Performer<LP>.Layout(
    delegate: Hikage.Delegate<*>,
    lparams: LayoutParams? = null,
    id: String? = null,
    embedded: Boolean = true
) = resolveContext().Layout(delegate, lparams, id, embedded)

/**
 * Create a new layout params.
 *
 * Usage:
 *
 * ```kotlin
 * View(
 *     lparams = LayoutParams {
 *         // You code here.
 *     }
 * )
 * ```
 * @see ViewLayoutParams
 * @param body the layout params body.
 * @return [LayoutParams]
 */
@Hikageable
fun <LP : ViewGroup.LayoutParams> Hikage.Performer<LP>.LayoutParams(
    width: Int? = null,
    height: Int? = null,
    matchParent: Boolean = false,
    widthMatchParent: Boolean = false,
    heightMatchParent: Boolean = false,
    body: LP.() -> Unit = {}
) = resolveContext().LayoutParams(width, height, matchParent, widthMatchParent, heightMatchParent, body)

