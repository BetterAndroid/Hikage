) = Layout(ViewBinding<VB>(), lparams, id)

/**
 * Provide layout from another [Hikage.Delegate].
 *
 * - Note: If you use [embedded], you must make sure that the introduced view IDs
 *   do not overlap with the view IDs in the current layout.
 * @receiver the delegate instance.
 * @param lparams the view layout params.
 * @param id the view id, generated by default.
 * @param embedded whether to embed this layout (All view IDs will be copied),
 * default is true.
 * @return [Hikage]
 */
@Hikageable
context(performer: Hikage.Performer<LP>)
operator fun <LP : ViewGroup.LayoutParams> Hikage.Delegate<*>.invoke(
    lparams: LayoutParams? = null,
    id: String? = null,
    embedded: Boolean = true
) = performer.Layout(this, lparams, id, embedded)

/**
 * Resolve the [PerformContext] from the [Hikage.Performer].
 * @receiver the performer instance.
 * @return [PerformContext]
 */
private fun <LP : ViewGroup.LayoutParams> Hikage.Performer<LP>.resolveContext() = this as? PerformContext<LP>
    ?: throw PerformerException("This Performer is not created by Hikage.")